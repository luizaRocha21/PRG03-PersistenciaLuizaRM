    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        String nome = txtNomeAluno.getText().trim();
        if (nome.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite um nome.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        modeloLista.addElement(nome);
        txtNomeAluno.setText("");
        txtNomeAluno.requestFocus();
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
         int index = listNomeAlunos.getSelectedIndex();
        if (index != -1) {
            modeloLista.remove(index);
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um aluno para remover.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnOrdenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrdenarActionPerformed
        List<String> lista = new ArrayList<>();
        for (int i = 0; i < modeloLista.size(); i++) {
            lista.add(modeloLista.getElementAt(i));
        }
        Collections.sort(lista, String.CASE_INSENSITIVE_ORDER);
        modeloLista.clear();
        for (String nome : lista) {
            modeloLista.addElement(nome);
        }
    }//GEN-LAST:event_btnOrdenarActionPerformed

    private void listNomeAlunosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listNomeAlunosMouseClicked
        if (evt.getClickCount() == 2) {
            int index = listNomeAlunos.locationToIndex(evt.getPoint());
            if (index != -1) {
                String antigo = modeloLista.getElementAt(index);
                String novo = JOptionPane.showInputDialog(this, "Editar nome do aluno:", antigo);
                if (novo != null && !novo.trim().isEmpty()) {
                    modeloLista.set(index, novo.trim());
                }
            }
        }
    }//GEN-LAST:event_listNomeAlunosMouseClicked

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // Atualiza a lista na tela que chamou
    if (parent instanceof CursoCadastrar cadastrar) {
        cadastrar.atualizarListaAlunos(getAlunosAtualizados());
        cadastrar.setVisible(true);
    } else if (parent instanceof CursoEditar editar) {
        editar.atualizarListaAlunos(getAlunosAtualizados());
        editar.setVisible(true);
    }

    // Fecha esta janela
    this.dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

